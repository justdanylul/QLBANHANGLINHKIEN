package Model;

public class hethang {
	public String mahang;
	public String ten;
	
	public hethang(String mahang, String ten) {
		super();
		this.mahang = mahang;
		this.ten = ten;
	}

	public String getMahang() {
		return mahang;
	}

	public void setMahang(String mahang) {
		this.mahang = mahang;
	}

	public String getTen() {
		return ten;
	}

	public void setTen(String ten) {
		this.ten = ten;
	}
	

}
